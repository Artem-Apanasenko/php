<?php
// ====== Завдання 1: Створення базового PHP-скрипта ======
/* 
 * Цей скрипт демонструє базовий синтаксис PHP
 * та виводить привітання на екран
 */
echo "<h2>Завдання 1:</h2>";
echo "Hello, World!"; // Виведення тексту на екран

// ====== Завдання 2: Змінні та типи даних ======
echo "<h2>Завдання 2:</h2>";
$string_var = "Це рядок"; // Рядкова змінна
$integer_var = 42; // Ціле число
$float_var = 3.14; // Число з плаваючою комою
$boolean_var = true; // Булеве значення

// Виведення значень змінних
echo "Значення змінних:<br>";
echo "Рядок: $string_var<br>";
echo "Ціле число: $integer_var<br>";
echo "Число з плаваючою комою: $float_var<br>";
echo "Булеве значення: $boolean_var<br><br>";

// Виведення типів змінних
echo "Типи змінних:<br>";
var_dump($string_var);
echo "<br>";
var_dump($integer_var);
echo "<br>";
var_dump($float_var);
echo "<br>";
var_dump($boolean_var);

// ====== Завдання 3: Конкатенація рядків ======
echo "<h2>Завдання 3:</h2>";
$first_name = "Іван";
$last_name = "Петренко";
echo "Повне ім'я: " . $first_name . " " . $last_name;

// ====== Завдання 4: Умовні конструкції ======
echo "<h2>Завдання 4:</h2>";
$number = 15;
if ($number % 2 == 0) {
    echo "Число $number є парним";
} else {
    echo "Число $number є непарним";
}

// ====== Завдання 5: Цикли ======
echo "<h2>Завдання 5:</h2>";
// Цикл for
echo "Числа від 1 до 10 (цикл for):<br>";
for ($i = 1; $i <= 10; $i++) {
    echo "$i ";
}
echo "<br><br>";

// Цикл while
echo "Числа від 10 до 1 (цикл while):<br>";
$j = 10;
while ($j >= 1) {
    echo "$j ";
    $j--;
}

// ====== Завдання 6: Масиви ======
echo "<h2>Завдання 6:</h2>";
// Створення асоціативного масиву
$student = array(
    "ім'я" => "Олександр",
    "прізвище" => "Коваленко",
    "вік" => 20,
    "спеціальність" => "Комп'ютерні науки"
);

// Виведення початкового масиву
echo "Інформація про студента:<br>";
foreach ($student as $key => $value) {
    echo "$key: $value<br>";
}

// Додавання нового елемента
$student["середній бал"] = 91.5;

// Виведення оновленого масиву
echo "<br>Оновлена інформація про студента:<br>";
foreach ($student as $key => $value) {
    echo "$key: $value<br>";
}
?>
